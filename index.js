// Load the contract
const contract = new ethers.Contract(CONTRACT_ADDRESS, contractAbi, signer);

// Function to mint an NFT
async function mintNft(recipientAddress, link) {
    try {
      // Estimate gas for minting the NFT
    //   const gasEstimate = await contract.estimateGas.mintNft(recipientAddress, link);
    //   console.log(gasEstimate)
  
      // Send the transaction to mint the NFT
      const tx = await contract.mintNFT(recipientAddress, link
    //     , {
    //     gasLimit: gasEstimate, // Manually estimated gas
    //     maxPriorityFeePerGas: ethers.parseUnits('2', 'gwei'), // Priority fee
    //     maxFeePerGas: ethers.parseUnits('40', 'gwei'), // Max fee
    //   }
    );

    //   console.log(tx)
  
      // Wait for the transaction to be mined
      const receipt = await tx.wait();
    console.log(receipt)
      return receipt.hash;
    } catch (error) {
      console.error("Error minting NFT:", error);
      throw error;
    }
  }
  
  
// Create the bot
const bot = new TelegramBot(BOT_TOKEN, { polling: true });

// Start command for the bot
bot.onText(/\/start/, async (msg) => {
  const user = msg.from;
  await bot.sendMessage(msg.chat.id, `Hello ${user.username}! Send me a link and your wallet address, and I will mint an NFT for you.`);
});

// Function to handle user messages (link + address)
bot.on('message', async (msg) => {
  const userMessage = msg.text.trim();
  
  // Check if the user provided both link and wallet address
  const messageParts = userMessage.split(" ");
  if (messageParts.length === 2 && messageParts[0].startsWith("http") && ethers.isAddress(messageParts[1])) {
    const link = messageParts[0];
    const recipientAddress = messageParts[1];

    try {
      // Check if the link has already been minted
      const isMinted = await contract.isLinkMinted(link);
      if (isMinted) {
        await bot.sendMessage(msg.chat.id, 'This link has already been minted as an NFT.');
      } else {
        // Mint the NFT
        const txHash = await mintNft(recipientAddress, link);
        await bot.sendMessage(msg.chat.id, `NFT minted! Transaction hash: ${txHash}`);
      }
    } catch (error) {
      await bot.sendMessage(msg.chat.id, `An error occurred while minting the NFT: ${error.message}`);
    }
  } else {
    await bot.sendMessage(msg.chat.id, 'Please send both a valid link and a wallet address, separated by a space.');
  }
});
